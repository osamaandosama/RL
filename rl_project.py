def softmax(x):
    x = x - x.max()
    e = np.exp(x)
    return e / e.sum()

class PortfolioEnv(gym.Env):
    def __init__(self, data, lookback=30, lam=0.5, tc=0.001, rf=0.0,
                 feature_mode='compact', start=None, end=None):
        super().__init__()
        self.prices, self.simple_ret = data['prices'], data['simple_ret']
        self.z_logret, self.z_vol = data['z_logret'], data['z_vol']
        self.compact = data['compact']
        self.N = self.prices.shape[1]
        self.lookback, self.lam, self.tc, self.rf = lookback, lam, tc, rf
        self.feature_mode = feature_mode
        self.vol_window = data.get('vol_window', 20)

        n = len(self.prices)
        self.start = lookback if start is None else max(int(start), lookback)
        self.end = n - 1 if end is None else int(end)

        obs_dim = (self.compact.shape[1] if feature_mode == 'compact'
                   else 3 * self.N * lookback) + (self.N + 1)
        self.observation_space = spaces.Box(-np.inf, np.inf, (obs_dim,), np.float32)
        self.action_space = spaces.Box(-1.0, 1.0, (self.N + 1,), np.float32)
    def reset(self, seed=None, options=None):
        super().reset(seed=seed)
        self.t = self.start
        self.w = np.zeros(self.N + 1); self.w[-1] = 1.0
        self.value = 1.0
        self.hist = deque(maxlen=self.vol_window)
        return self._obs(), {}
    def _obs(self):
        if self.feature_mode == 'compact':
            feat = self.compact[self.t]
        else:
            lo, hi = self.t - self.lookback + 1, self.t + 1
            p = self.prices[lo:hi]; p = p / p[0]
            feat = np.concatenate([p.ravel(), self.z_logret[lo:hi].ravel(),
                                   self.z_vol[lo:hi].ravel()])
        return np.concatenate([feat, self.w]).astype(np.float32)
    def step(self, action):
        w = softmax(np.asarray(action, dtype=float).ravel())
        risky, risky_prev = w[:self.N], self.w[:self.N]
        cost = self.tc * np.abs(risky - risky_prev).sum()
        port_ret = float(risky @ self.simple_ret[self.t + 1] + w[self.N] * self.rf)
        self.value *= 1 + port_ret - cost
        self.hist.append(port_ret)
        pvol = float(np.std(self.hist)) if len(self.hist) > 1 else 0.0
        reward = port_ret - self.lam * pvol - cost
        self.w, self.t = w, self.t + 1
        done = bool(self.t >= self.end)
        return self._obs(), reward, False, done, {
            'value': self.value, 'ret': port_ret, 'cost': cost, 'weights': self.w.copy()}

def make_envs(data, lookback, lam, feature_mode='compact', tc=0.001):
    tr, te = np.where(data['train_mask'])[0], np.where(data['test_mask'])[0]
    common = dict(data=data, lookback=lookback, lam=lam, tc=tc, feature_mode=feature_mode)
    train = PortfolioEnv(start=max(lookback, int(tr[0])), end=int(tr[-1]), **common)
    test = PortfolioEnv(start=max(lookback, int(te[0])), end=int(te[-1]), **common)
    return train, test

