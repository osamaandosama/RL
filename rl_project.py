"""Download adjusted prices, build the feature series, and standardize returns and volatility with a **causal trailing-window z-score** (uses only past data, so no leakage).

"""

def causal_zscore(df, window=252, min_periods=20):
    m = df.rolling(window, min_periods=min_periods).mean()
    s = df.rolling(window, min_periods=min_periods).std()
    return ((df - m) / (s + 1e-8)).fillna(0.0)

def get_data(tickers, start, end, vol_window, split, z_window=252):
    px = yf.download(tickers, start=start, end=end, auto_adjust=True,
                     progress=False)['Close'][tickers].dropna()
    simple = px.pct_change()
    logr = np.log(px / px.shift(1))
    vol = logr.rolling(vol_window).std()
    r5 = np.log(px / px.shift(5))
    r21 = np.log(px / px.shift(21))
    z_logret, z_vol = causal_zscore(logr, z_window), causal_zscore(vol, z_window)
    zc = {k: causal_zscore(v, z_window)
          for k, v in {'r1': logr, 'r5': r5, 'r21': r21, 'v21': vol}.items()}
    keep = vol.dropna().index.intersection(r21.dropna().index)
    sl = lambda d: d.loc[keep]
    px, simple, logr, vol = sl(px), sl(simple), sl(logr), sl(vol)
    z_logret, z_vol = sl(z_logret), sl(z_vol)
    compact = np.concatenate([sl(zc['r1']).values, sl(zc['r5']).values,
                              sl(zc['r21']).values, sl(zc['v21']).values], axis=1)
    train = px.index < pd.Timestamp(split)
    return {'tickers': list(px.columns), 'dates': px.index, 'prices': px.values,
            'simple_ret': simple.values, 'log_ret': logr.values, 'vol': vol.values,
            'z_logret': z_logret.values, 'z_vol': z_vol.values, 'compact': compact,
            'train_mask': train, 'test_mask': ~train, 'vol_window': vol_window}

