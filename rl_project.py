def max_sharpe(mu, cov):
    N = len(mu)
    neg = lambda w: -(w @ mu) / (np.sqrt(w @ cov @ w) + 1e-12)
    cons = ({'type': 'eq', 'fun': lambda w: w.sum() - 1},)
    r = minimize(neg, np.ones(N) / N, method='SLSQP', bounds=[(0, 1)] * N,
                 constraints=cons, options={'maxiter': 500, 'ftol': 1e-9})
    w = np.clip(r.x, 0, None)
    return w / w.sum()

def equal_weight(simple, s, e, tc=0.001):
    N = simple.shape[1]; tgt = np.ones(N) / N; w = np.zeros(N); out = []
    for t in range(s, e):
        cost = tc * np.abs(tgt - w).sum(); w = tgt.copy()
        out.append(float(w @ simple[t + 1]) - cost)
        g = w * (1 + simple[t + 1]); w = g / g.sum()
    return np.array(out)

def markowitz(simple, s, e, window=252, rebal=21, tc=0.001):
    N = simple.shape[1]; w = np.zeros(N); out = []
    for t in range(s, e):
        if (t - s) % rebal == 0:
            past = simple[max(0, t - window):t]
            tgt = max_sharpe(past.mean(0) * 252, np.cov(past.T) * 252)
            cost = tc * np.abs(tgt - w).sum(); w = tgt
        else:
            cost = 0.0
        out.append(float(w @ simple[t + 1]) - cost)
        g = w * (1 + simple[t + 1]); w = g / g.sum()
    return np.array(out)

def buy_and_hold(simple, s, e, idx):
    return simple[s + 1:e + 1, idx].astype(float).copy()

def backtest(model, env):
    obs, _ = env.reset(); done = False; rets, ws = [], []
    while not done:
        a, _ = model.predict(obs, deterministic=True)
        obs, _, term, trunc, info = env.step(a); done = term or trunc
        rets.append(info['ret'] - info['cost']); ws.append(info['weights'])
    return np.array(rets), np.array(ws)

def metrics(rets):
    rets = np.asarray(rets, float)
    cum = float(np.prod(1 + rets) - 1)
    yrs = len(rets) / 252
    cagr = (1 + cum) ** (1 / yrs) - 1 if (1 + cum) > 0 and yrs > 0 else np.nan
    vol = float(rets.std() * np.sqrt(252))
    sharpe = float(rets.mean() / (rets.std() + 1e-12) * np.sqrt(252))
    eq = np.cumprod(1 + rets); dd = float((eq / np.maximum.accumulate(eq) - 1).min())
    down = rets[rets < 0]
    sortino = float(rets.mean() / (down.std() + 1e-12) * np.sqrt(252)) if len(down) else np.nan
    calmar = float(cagr / abs(dd)) if (dd < 0 and not np.isnan(cagr)) else np.nan
    return {'Total Return': cum, 'CAGR': cagr, 'Volatility': vol, 'Sharpe': sharpe,
            'Sortino': sortino, 'Max Drawdown': dd, 'Calmar': calmar}

